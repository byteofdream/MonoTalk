function getLang(): string {
    $allowed = ['ru', 'en'];
    if (isset($_GET['lang']) && in_array($_GET['lang'], $allowed, true)) {
        $lang = $_GET['lang'];
        $_SESSION['lang'] = $lang;
        setcookie('lang', $lang, time() + 60 * 60 * 24 * 365, '/');
        return $lang;
    }
    if (isset($_SESSION['lang']) && in_array($_SESSION['lang'], $allowed, true)) {
        return $_SESSION['lang'];
    }
    if (isset($_COOKIE['lang']) && in_array($_COOKIE['lang'], $allowed, true)) {
        $_SESSION['lang'] = $_COOKIE['lang'];
        return $_COOKIE['lang'];
    }
    $_SESSION['lang'] = 'ru';
    return 'ru';
}

function t(string $key): string {
    global $translations;
    $lang = getLang();
    return $translations[$lang][$key] ?? $translations['ru'][$key] ?? $key;
}

function catName(array $cat, string $lang): string {
    $key = $lang === 'en' ? 'name_en' : 'name';
    return $cat[$key] ?? $cat['name'] ?? '';
}
